# 분이 45분 미만이면 b < 45가 참이 되므로 시 부분이 1 줄어야 한다. a-(b < 45) 값을 24로 나눈 나머지가 시가 된다. (ex) 0 % 24 == 0, -1 % 24 == 23, 13 % 24 == 13)
# 분의 경우도 45를 빼고 60으로 나눈 나머지가 분이 됨. (ex) (35 - 45) % 60 = 50, (50 -45) % 60 = 5)