H, M = map(int, input().split())

if H != 0:     # 시가 0이 아닐 때
  if M >= 45:     # 분이 45분 이상일 때
    M -= 45
  else:     # 분이 45분 미만일 때
    H -= 1
    M = M - 45 + 60
else:     # 시가 0일 때
  if M >= 45:     # 분이 45분 이상일 때
    M -= 45
  else:     # 분이 45분 미만일 때
    H = 23
    M = M - 45 + 60 

print(H, M)

